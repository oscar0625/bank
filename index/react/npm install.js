/*
*
* npm
     Node Package Manager
     package.json 存储依赖包的文件

     terminal:alt+f12(等同于cmd)

     cmd路径：前进 cd 文件名 后退：cd..

     cmd指令:
        install 下载
         npm install [jquery] 下载某一个依赖
         npm install          下载所有依赖, 自动读取 package.json 文件

        uninstall 卸载

     -g          全局指令
     --save / -s 将操作记录到 package.json

     init
     初始化项目, 生成 package.json文件

     是否安装node
        cmd => node -v
            => npm -v

     是否安装cnpm
     安装方法 ：npm install -g cnpm --registry=https://registry.npm.taobao.org
        cmd => cnpm -v
 */